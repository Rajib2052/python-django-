
# Define the list of numbers
numbers = [1,9,6,5,89,1203,4,23]

# Find the highest number using the max() function
highest_number = max(numbers)

# Print the result
print("The highest number is:", highest_number)
# Sort the list in ascending order
numbers.sort()

# Print the sorted list
print("Sorted list:", numbers)

# Find the highest number using the last element of the sorted list
highest_number = numbers[-1]

# Print the highest number
print("The highest number is:", highest_number)

